export default function DocumentContent({
  section,
  searchQuery,
}: DocumentContentProps) {
  const currentContent = contentMap[section] || contentMap.overview;

  // Simple search highlighting
  let displayContent = currentContent.content;
  if (searchQuery) {
    const regex = new RegExp(`(${searchQuery})`, "gi");
    displayContent = displayContent.replace(
      regex,
      '<mark style="background-color: #fbbf24; padding: 0 2px;">$1</mark>'
    );
  }

  return (
    <div className="prose prose-invert max-w-none">
      <h1 className="text-3xl font-bold mb-6 text-foreground">
        {currentContent.title}
      </h1>

      {section === "architecture" && <ArchitectureImage />}

      <Streamdown>{displayContent}</Streamdown>
    </div>
  );
}
